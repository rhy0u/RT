#include "rt.h"

Uint32		rgb(t_xyz c)
{
	if (c.x > 255)
		c.x = 255;
	if (c.y > 255)
		c.y = 255;
	if (c.z > 255)
		c.z = 255;
	if (c.x < 0)
		c.x = 0;
	if (c.y < 0)
		c.y = 0;
	if (c.z < 0)
		c.z = 0;
	return (((unsigned int)c.x * 256 + (unsigned int)c.y) * 256 +
			(unsigned int)c.z);
}
